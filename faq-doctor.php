						<strong> How long is the typical locum tenens assignment?</strong> </br>
						Assignments can vary from one day to over a year. In general, assignment lengths tend to range from a weekend, to a few months. Sometimes, it can be a couple of days a week, every week, for many months. The details will be explained to you before you sign up to any assignment.
					</p>

					</br>
					<P>
						<strong> Do I have a choice in selecting where I go?</strong></br>
						Yes. As an independent contractor, you can select where and when you wish to practice. You may select from whatever opportunities the agency presents to you and choose which, if any, assignment you want to take. Medline will not attempt to dictate where and when you work.
					</P>

						</br>

					<p>
						<strong> What type of support services does Medline provide?</strong></br>
				Medline will not only get you the assignment, it will support you all the way, so that you can put in your best. You can be sure you will get:

				<ul style="list-style-type:disc">
				  <li>Regular assignments</li>
				  <li>Credentialing assistance</li>
				  <li>Appropriate matching of doctor to assignment</li>
				  <li>Assignment orientation</li>
				  <li>Branded clinical coats</li>
				  <li>Initial travel to your assignment</li>
				  <li>Regular monitoring during your assignment</li>
				  <li>Full logistical support</li>
				  <li>Prompt payment, after Medline gets your timesheet</li>
				</ul>

“Full logistical support” entails ensuring that the doctor’s assignment goes as smoothly as possible and that all details regarding licensure, credentialing, travel, and so on are handled efficiently. Medline Locum Agency will work to ensure that the doctor’s skills and availability are compatible with the requirements of the agency’s client hospital.

					</p>


							</br>

					<p>
						<strong> WHAT DOCUMENTS ARE NEEDED TO GET ON YOUR DATA BASE?</strong></br>
						The usual documents that you will need for a permanent employment. Your certificates, registration with MDCN, and current renewal.  You will see details in the registration page
					</p>

					</br>

					<p>
						<strong>HOW MUCH DO I GET PAID?</strong></br>

						What you get paid is what we are able to negotiate with our client hospitals, minus our agency commission. The real amount depends on your level of specialisation and experience, which we would have shown to the client hospital.  There are three grades:  


				<ul style="list-style-type:disc">
						  <li>Medical Officer/Registrar</li>
						  <li>Senior Medical Officer/Senior Registrar<li>
						  <li>Consultant/Specialist</li>
				</ul>

				During the credentialing, we will place you in the appropriate category, and pay you for the grade. Calculation is done hourly, or per shift. A standard day shift is 8 hours.  A standard night shift is 12 hours.  For specialists, payment may be per session, which is four hours.  Details will be in the assignment contract.  Contact us for more details and fine prints.

					</p>

						</br>

					<p>
						<strong> WHEN DO I GET PAID</strong></br>
						You get paid latest in the first week of the month after your assignment is completed and you submit your timesheet. Most providers get paid much earlier, within a week of receiving your timesheet.  We do some verification and reconciliation with the client hospital after which we credit the provider’s account.  All payments are done by check or bank transfer. 
					</p>

						</br>

					<p>
						<strong> CAN I REFUSE ANY OFFER?</strong></br>
						Yes.  That is the joy with locum tenens jobs.  You can pick and choose as the offers come.  We try to match the assignments with your availability and skills as in our data base.  Please update your availability details as soon as they change, so that you don’t miss out on offers.
					</p>


						</br>


					<p>
						<strong> CAN I BACK OUT HALF WAY THROUGH AN ASSIGNMENT?</strong></br>
						We will prefer you make a complaint earlier, if it is an issue with the assignment, and you let us take it up with the client hospital.  It damages the relationship with the hospital, and we may have difficulty getting payment from the hospital for the part of the assignment you have done.  If it is inevitable however, please contact us as soon as possible so that we can arrange a replacement for the assignment.  You will only get paid for the fraction of the assignment done minus any additional costs incurred in completing the assignment.  Your ability to get new offers may be affected.
					</p>

						</br>

					<p>
						<strong> WHY YEARLY SUBSCRIPTION?</strong></br>
						It is necessary for providers to confirm their availability yearly, so that our data base can be as current as possible.  Doctors who have since moved on, or whose circumstances have changed will not need to still have their name on our data base.  The subscription fee will be nominal, N5000 or less, and it can even be waived for our seasoned providers who get great reviews from client hospital for their assignments.
