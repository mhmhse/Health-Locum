					<h5 style="text-decoration:underline;">General CV Rules:</h3>

					<p>
						<strong>The Format :</strong></br>
						Type and save your CV using Microsoft Word. Make sure that your CV can be attached and read clearly in an email. You'll want to send your CV as an Acrobat Reader file (.pdf – so changes cannot be made to it) and also a Word file (for Medline to keep and update, as necessary.) Scanned paper copies will need to be reprepared by our staff to formats suitable for electronic transmission	
					<p>
						
					<p>
						<strong>The Look :</strong></br>
						Remember that your CV will likely be downloaded, updated, and repeatedly sent in various formats to client hospitals, so you should use a universal font (such as Times New Roman or Arial) and size the text to 12-pitch. Use black ink ONLY to make your text as easy to read as possible.
					</p>


					<p>
						<strong>The Structure :</strong></br>
						
						<ol>
							
							<li><p>
								<strong>Contact and personal Information</strong></br>
								Your CV should begin with full name (followed by MBBS and relevant credentials – FMCOG, FRCS etc.), address, phone numbers (home, work and cell) and email address. If applicable, you can indicate your specialty. In this area you'll put your marital status, number of children (if any;optional) and your birth date
							</p>
							</li>

							<li>
								<p>
									<strong>Education and Training</strong></br>
									In reverse chronological order (most recent listed first) list fellowship and residency training, medical school, and college (include degree and major) names and locations and the dates degrees were acquired. If you did your residency at multiple locations, be sure to include the dates when you were at each institution.
								</p>	
							</li>


							<li>
								<p>
										<strong>Professional Experience</strong>
										List your work experience in reverse chronological order as well. If you have held several positions within the same hospital organization but have not stayed in one location, make it clear that these were not separate employers. You do not want it to appear you have switched employers on a regular basis if this is not the case. Make it clearer that you have moved with the same employer by listing the name of the hospital and then indenting the positions.
								</p>

								<p>
									Make sure there are no unaccounted gaps between your years of experience. If you took a year off, provide a short explanation about the gap. It is helpful if you use mm/yy format to account for any gaps over 30 days on your CV.
								</p>

								<p>
									Locum Tenens assignments should all be listed under a separate Locum Tenens Experience header within this section. List these in reverse chronological order as well. Include the name of the facility, city and state.
								</p>							
							</li>


							<li>
								<p>
									<strong>Awards</strong></br>
									This is where you should indicate any work-related recognitions or awards. If you received a major college scholarship, Dean’s list, this would be the place to list that information.
								</p>
							</li>


							<li>
								<p>
									<strong>Other Education and Training</strong></br>
										Include any other relevant education or training. For example:
								</p>	

								<p style="font-style: oblique;">
									Course in Psychiatry — Family Practice, UCH, Ibadan, June 2008
								</p>

								<p>
									Some people like to include their training in areas like sonography, major CMD courses, computer programmes, and various electronic medical record keeping software, etc., here. Indicate the specific dates so the employer knows how recent your training is. Indicate if you are an instructor in any of the areas.
								</p>
							</li>


							<li>
								<p>
									<strong>Publications and Presentations</strong></br>
										If you only have a few to include, you can place them between the Education and Other Training and Personal Information headings. If you have more than half a page worth of publications and presentations (particularly since it is a Clinical CV) then you should create a separate page for them.
								</p>
							</li>

							<li>
								<p>
								<strong>Other Personal. </strong></br>
								It is also a good idea to note any special interests and hobbies you have.</p>

								<p>
									Including some of these types of personal information helps break the ice in your interview and gives employers something to talk about with you.
								</p>	
							</li>


							<li>
								<p>
									<strong>References</strong></br>
								Try to include at least the current or last employer or person you reported to at work.  Make sure their physical and email addresses, and phone numbers are current and active.  Medline will definitely contact them and any difficulty in correspondence will delay your getting locum placements. 
								</p>

								<p>Be sure your references are aware that you’ve listed them as a reference before they are contacted so they can be ready to provide relevant comments about you.</p>
							</li>
							
							
						</ol>						
