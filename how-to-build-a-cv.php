					<p>
						The purpose of your Curriculum Vitae (a Latin phrase meaning “course of life,” CV for short) is to introduce yourself to prospective facilities. The CV concisely provides all of your pertinent clinical, educational, and personal information to clinical decision makers. It is an important component for securing a locum tenens — or any medical position — and should be updated regularly to reflect your medical preferences, practices and achievements. Medline will send a modified version (excluding selected personal details) to prospective clients.
					</p>


				
					<p>
						Ideally, your Clinical CV should be about two or three pages in length although those physicians who have practiced for many years and are well published may have longer CVs.
					</p>

					<p></p>
					<p></p>
